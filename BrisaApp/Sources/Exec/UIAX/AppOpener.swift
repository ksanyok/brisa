import AppKit

/// Исполнитель для открытия приложений. Использует NSWorkspace для запуска приложения
/// по его названию. В будущих версиях может использовать анализ скриншотов и UI
/// автоматизацию для поиска и клика по иконке.
final class AppOpener {
    /// Открывает приложение с указанным именем. Например, "Telegram".
    func open(appName: String) {
        // Пытаемся запустить приложение через NSWorkspace. Если приложение
        // установлено, оно будет открыто. Если нет — ничего не произойдет.
        NSWorkspace.shared.launchApplication(appName)

        // TODO: При необходимости добавить анализ скриншота экрана для
        // поиска и клика по иконке приложения в Dock.
    }
}